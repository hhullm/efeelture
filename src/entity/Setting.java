package entity;

public class Setting {
	
	private String id;
	private String uid;
	private String topbutton;
	private String personbutton;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getTopbutton() {
		return topbutton;
	}
	public void setTopbutton(String topbutton) {
		this.topbutton = topbutton;
	}
	public String getPersonbutton() {
		return personbutton;
	}
	public void setPersonbutton(String personbutton) {
		this.personbutton = personbutton;
	}
	

	

}
