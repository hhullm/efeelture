package entity;

public class Hardware {
	
	String id;
	String uid;
	String hardwareid;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getHardwareid() {
		return hardwareid;
	}
	public void setHardwareid(String hardwareid) {
		this.hardwareid = hardwareid;
	}
	
	

}
