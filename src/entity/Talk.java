package entity;

public class Talk {
	
	String tid;
	String tfirstid;
	String tsecondid;
	int ttype;
	String tcontent;
	
	

	public String getTid() {
		return tid;
	}



	public void setTid(String tid) {
		this.tid = tid;
	}



	public String getTfirstid() {
		return tfirstid;
	}



	public void setTfirstid(String tfirstid) {
		this.tfirstid = tfirstid;
	}



	public String getTsecondid() {
		return tsecondid;
	}



	public void setTsecondid(String tsecondid) {
		this.tsecondid = tsecondid;
	}



	public int getTtype() {
		return ttype;
	}



	public void setTtype(int ttype) {
		this.ttype = ttype;
	}



	public String getTcontent() {
		return tcontent;
	}



	public void setTcontent(String tcontent) {
		this.tcontent = tcontent;
	}



}
