package entity;

public class Like {
	
	private String lid;
	private String uid;
	private String mid;


	public String getLid() {
		return lid;
	}



	public void setLid(String lid) {
		this.lid = lid;
	}



	public String getUid() {
		return uid;
	}



	public void setUid(String uid) {
		this.uid = uid;
	}



	public String getMid() {
		return mid;
	}



	public void setMid(String mid) {
		this.mid = mid;
	}



}
