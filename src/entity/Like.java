package entity;

public class Like {
	
	private String id;
	private String uid;
	private String messageid;


	public String getLid() {
		return id;
	}



	public void setLid(String lid) {
		this.id = lid;
	}



	public String getUid() {
		return uid;
	}



	public void setUid(String uid) {
		this.uid = uid;
	}



	public String getMid() {
		return messageid;
	}



	public void setMid(String mid) {
		this.messageid = mid;
	}



}
